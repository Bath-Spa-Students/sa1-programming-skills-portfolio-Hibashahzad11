# Loop 1: Count up from 0 to 50 in increments of 1
print("Loop 1: Count up from 0 to 50")
for a in range(0, 51):
    print(a)

# Loop 2: Count down from 50 to 0 in decrements of 1
print("\nLoop 2: Count down from 50 to 0")
for a in range(50, -1, -1):
    print(a)

# Loop 3: Count up from 30 to 50 in increments of 1
print("\nLoop 3: Count up from 30 to 50")
for a in range(30, 51):
    print(a)

# Loop 4: Count down from 50 to 10 in decrements of 2
print("\nLoop 4: Count down from 50 to 10 in steps of 2")
for a in range(50, 9, -2):
    print(a)

# Loop 5: Count up from 100 to 200 in increments of 5
print("\nLoop 5: Count up from 100 to 200 in steps of 5")
for a in range(100, 201, 5):
    print(a)